# Características de las listas en Python

mi_lista=[1,2,3]
print("Lista original:", mi_lista)

#Las listas son mutables, es decir, se pueden modificar sus elementos
mi_lista[0] = 10
print("Lista después de modificar el primer elemento:", mi_lista)

#Las listas permiten elementos de diferentes tipos de datos
mi_lista= ["a", "b", "c"]
print("Lista ordenada:", mi_lista)

#Las listas permiten elementos repetidos
mi_lista= [1,2,2,3,3,3]
print("Lista con elementos repetidos:", mi_lista)