# Tipos de Operaciones

# Operaciones Aritméticas
a = 10
b = 3

print("Suma:", a + b)          # Suma
print("Resta:", a - b)         # Resta
print("Multiplicación:", a * b) # Multiplicación
print("División:", a / b)       # División
print("División entera:", a // b) # División entera
print("Módulo:", a % b)         # Módulo arroja el restante de una división
print("Potencia:", a ** b)      # Potencia
print("Raíz cuadrada de a:", a ** 0.5) # Raíz cuadrada

x = int(input("Ingrese un valor para x: "))
y = int(input("Ingrese un valor para y:"))

print(f"La suma de {x} + {y} es: {x + y}")
print(f"La resta de {x} - {y} es: {x - y}")
print(f"La multiplicación de {x} * {y} es: {x * y}")
print(f"La división de {x} / {y} es: {x / y}")      
print(f"La división entera de {x} // {y} es: {x // y}")
print(f"El módulo de {x} % {y} es: {x % y}")
print(f"{x} elevado a la {y} es: {x ** y}")
print(f"La raíz cuadrada de {x} es: {x ** 0.5}")
print(f"La raíz cuadrada de {y} es: {y ** 0.5}")


